public class Section1503 {

    public static void main(String[] args) {
        // int型の配列scoreArrayを宣言し、10,20,30,40を代入する。
        int[] scoreArray={12,20,30,40};
        // 拡張for文を使って、すべての要素を表示する
        for(int array: scoreArray){
            System.out.println(array);
        }
    }
}