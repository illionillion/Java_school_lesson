public class Section0502 {

    public static void main(String[] args) {
        // 「"」はダブルクォートと読みます。と表示
        System.out.println("「\"」はダブルクォートと読みます。");
        // 「'」はシングルクォートと読みます。と表示
        System.out.println("「\'」はシングルクォートと読みます。");
        // 「\」はバックスラッシュと読みます。と表示
        System.out.println("「\\」はバックスラッシュと読みます。");

        System.out.println("「\n」は改行。");
    }
}