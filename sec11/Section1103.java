public class Section1103 {

    public static void main(String[] args) {
        // 100を代入できる変数scoreを宣言
        int score=100;
        // 変数scoreを使い、「scoreの中身は100です。」と表示
        System.out.println("scoreの中身は"+score+"です。");
        // trueを代入できる変数flagを宣言
        boolean flag=true;
        //変数flagを使い、「flagの中身はtrueです。」と表示
        System.out.println("flagの中身は"+flag+"です。");
        // 55.3を代入できる変数weightを宣言
        double weight=55.3;
        // 変数weightを使い、「weightの中身は55.3です。」と表示
        System.out.println("weightの中身は"+weight+"です。");
        // 文字列ABCDEFGを代入できる変数strを宣言
        String str="ABCDEFG";
        // 変数strを使い、「strの中身はABCDEFG」と表示
        System.out.println("strの中身は"+str);
    }
}