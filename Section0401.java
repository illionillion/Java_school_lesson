public class Section0401 {

    public static void main(String[] args) {
        // 整数を格納するデータ型で変数名「salary」を宣言し、100000を代入
        int salary;
        salary=100000;
        // 小数を格納するデータ型で変数名「weight」を宣言し、50.5を代入
        double weight;
        weight=50.5;
        // 真偽値を格納するデータ型で変数名「isOdd」を宣言し、Trueを代入
        boolean isOdd;
        isOdd=true;
        // 1文字だけを格納するデータ型変数名「initial」を宣言し、'A'を代入
        char initial;
        initial='A';
        // 文字列を格納するデータ型で変数名「name」を宣言し、"Java"を代入
        String name;
        name="Java";
        System.out.println(salary+"\n"+weight+"\n"+isOdd+"\n"+initial+"\n"+name);
    }
}